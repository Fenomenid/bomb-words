function buildDictionary(baseWords: string[], contexts: string[]): string[] {
  const words = [...baseWords];

  for (const context of contexts) {
    for (const word of baseWords) {
      words.push(`${context} ${word}`);
    }
  }

  return [...new Set(words)].slice(0, 500);
}
